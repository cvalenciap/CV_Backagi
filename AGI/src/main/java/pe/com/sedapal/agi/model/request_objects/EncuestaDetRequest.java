package pe.com.sedapal.agi.model.request_objects;

public class EncuestaDetRequest {
	
	private Long iniddetaencu;
	private Long inidencu;
	private String ivdespre;
	private String ivcodetaencu;
	
	public Long getIniddetaencu() {
		return iniddetaencu;
	}
	public void setIniddetaencu(Long iniddetaencu) {
		this.iniddetaencu = iniddetaencu;
	}
	public Long getInidencu() {
		return inidencu;
	}
	public void setInidencu(Long inidencu) {
		this.inidencu = inidencu;
	}
	public String getIvdespre() {
		return ivdespre;
	}
	public void setIvdespre(String ivdespre) {
		this.ivdespre = ivdespre;
	}
	public String getIvcodetaencu() {
		return ivcodetaencu;
	}
	public void setIvcodetaencu(String ivcodetaencu) {
		this.ivcodetaencu = ivcodetaencu;
	}
	
}
