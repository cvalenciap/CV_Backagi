package pe.com.sedapal.agi.model.request_objects;

public class EncuestaRequest {
	
	private Long inidcurs;  
	private Long  inidencu;
	private String ivcodencu;
	private String ivnomencu;
	
	public Long getInidcurs() {
		return inidcurs;
	}
	
	public void setInidcurs(Long inidcurs) {
		this.inidcurs = inidcurs;
	}
	
	public Long getInidencu() {
		return inidencu;
	}
	
	public void setInidencu(Long inidencu) {
		this.inidencu = inidencu;
	}
	
	public String getIvcodencu() {
		return ivcodencu;
	}
	
	public void setIvcodencu(String ivcodencu) {
		this.ivcodencu = ivcodencu;
	}
	public String getIvnomencu() {
		return ivnomencu;
	}
	public void setIvnomencu(String ivnomencu) {
		this.ivnomencu = ivnomencu;
	}  
	
}
