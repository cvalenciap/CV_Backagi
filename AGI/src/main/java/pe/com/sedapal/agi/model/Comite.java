package pe.com.sedapal.agi.model;

public class Comite {
	private String valorComite;
	private String descripcionComite;
	
	public String getValorComite() {
		return valorComite;
	}
	public void setValorComite(String valorComite) {
		this.valorComite = valorComite;
	}
	public String getDescripcionComite() {
		return descripcionComite;
	}
	public void setDescripcionComite(String descripcionComite) {
		this.descripcionComite = descripcionComite;
	}
	
	
}
